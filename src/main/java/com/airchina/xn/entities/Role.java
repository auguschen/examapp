package com.airchina.xn.entities;

public class Role {

	private String role;
	private String name;
	
	public Role(){
		
	}
	
	public Role(String role){
		this.role = role;
	}

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
